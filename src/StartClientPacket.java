public class StartClientPacket {
    private String name;
    private String characterSprite;
    private String carSprite;

    StartClientPacket() {

    }

    public String getName() {
        return name;
    }

    public String getCharacterSprite() {
        return characterSprite;
    }

    public String getCarSprite() {
        return carSprite;
    }
}
